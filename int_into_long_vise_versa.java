package Program_practise;

public class int_into_long_vise_versa {

	public static void main(String[] args) {
		
		//int into long
		int num1 = 123;
		long num2 = num1;
		System.out.println(num2);
		
		
		//long into int
		long num3=12345;
		int num4=(int)num3;
		System.out.println(num4);
		

	}

}
